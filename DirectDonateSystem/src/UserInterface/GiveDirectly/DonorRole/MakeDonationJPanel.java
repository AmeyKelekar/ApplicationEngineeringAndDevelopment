    private void cardNumberJTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cardNumberJTextField1KeyTyped
        // TODO add your handling code here:
        char character = evt.getKeyChar();
        int length = cardNumberJTextField1.getText().length();
        if(!(Character.isDigit(character) || character == KeyEvent.VK_BACK_SPACE || character == KeyEvent.VK_DELETE)
                || length >= 4){
            getToolkit().beep();
            evt.consume();
        }
        if (length < 4) {
            cardNumberJTextField1.requestFocus();
        }
        else{
            cardNumberJTextField1.transferFocus();
        }
      

    }//GEN-LAST:event_cardNumberJTextField1KeyTyped

