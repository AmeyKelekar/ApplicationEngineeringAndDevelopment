    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        if (amountJTextField.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter the amount you wish to donate!!!");
            amountJTextField.setText("");
            amountJTextField.requestFocus();
        }
        else if((visaCardJRadioButton.isSelected() == false) &&(masterCardJRadioButton.isSelected() == false)
                &&(americanExpressJRadioButton.isSelected() == false)){
            JOptionPane.showMessageDialog(null, "Please select a card Type!!!");
        }
        else if (cardNumberJTextField1.getText().trim().equals("")
                || cardNumberJTextField2.getText().trim().equals("")
                || cardNumberJTextField3.getText().trim().equals("")
                || cardNumberJTextField4.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter the correct Card Number!!!");
            cardNumberJTextField1.setText("");
            cardNumberJTextField2.setText("");
            cardNumberJTextField3.setText("");
            cardNumberJTextField4.setText("");
            cardNumberJTextField1.requestFocus();
        }
        else if (cardNumberJTextField1.getText().length() < 4
                || cardNumberJTextField2.getText().length() < 4
                || cardNumberJTextField3.getText().length() < 4
                || cardNumberJTextField4.getText().length() < 4) {
            JOptionPane.showMessageDialog(null, "Please enter the correct Card Number!!!");
            cardNumberJTextField1.setText("");
            cardNumberJTextField2.setText("");
            cardNumberJTextField3.setText("");
            cardNumberJTextField4.setText("");
            cardNumberJTextField1.requestFocus();
        }
        else if (cvvJTextField.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter the CVV!!!");
            cvvJTextField.setText("");
            cvvJTextField.requestFocus();
        }
        else if (cvvJTextField.getText().length() <3) {
            JOptionPane.showMessageDialog(null, "Please enter the correct CVV!!!");
            cvvJTextField.setText("");
            cvvJTextField.requestFocus();
        }
        else if (((JTextField)expiryDateJDateChooser.getDateEditor().getUiComponent()).getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter the expiry date!!!");
            expiryDateJDateChooser.requestFocus();
        }
        else if (Float.parseFloat(amountJTextField.getText()) > 500000){
            JOptionPane.showMessageDialog(null, "You can only donate $5,00,000 at a time!!!");
            amountJTextField.setText("");
            amountJTextField.requestFocus();
        }
        else{
            try{
                FinanceAccount financeAccount = FinanceAccount.getInstance();
    //            float sumTotal = financeAccount.getBalanceAmount();
                float amountDonated = 
                        Float.parseFloat(amountJTextField.getText());
    //            financeAccount.setBalanceAmount(sumTotal+amountDonated);
                financeAccount.setTimestamp(new Date());

                financeAccount.updateDirectory(amountDonated,userAccount, null);

                Transaction transaction = giveDirectlyEnterprise.
                        getTransactionDirectory().addTransaction();
                transaction.setSender((Donor)userAccount.getPerson());
                transaction.setAmount(amountDonated);
                transaction.setReceiver("GiveDirectly");
                transaction.setTransactionID(transaction.getTransactionID());
                transaction.setTimeStamp(new Date());
                System.out.println(financeAccount.getBalanceAmount());
            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null,
                        "Please insert valid number value!!!");
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,
                        "There's some error with database,please try again later!!");
            }
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

